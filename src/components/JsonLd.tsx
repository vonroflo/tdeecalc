import { generatePageSchema } from '@/lib/seo';

export default function JsonLd() {
  const schemas = generatePageSchema();

  return (
    <>
      {schemas.map((schema, index) => (
        <script
          key={index}
          type="application/ld+json"
          dangerouslySetInnerHTML={{
            __html: JSON.stringify(schema),
          }}
        />
      ))}
    </>
  );
}
